#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const args = new Set(process.argv.slice(2));
const reportPath = path.join(root, ".agent-estate/report.json");
const outputPath = path.join(root, ".agent-estate/report.md");

function readJson(filePath) {
  return JSON.parse(readFileSync(filePath, "utf8"));
}

function bullet(value) {
  return `- ${value}`;
}

function riskBadge(level) {
  return level.toUpperCase();
}

function renderEvidence(evidenceItems) {
  if (!evidenceItems?.length) {
    return "  - Evidence: none recorded";
  }

  return evidenceItems
    .map((item) => {
      const pathText = item.path ? ` (${item.path})` : "";
      return `  - Evidence: ${item.label}${pathText}`;
    })
    .join("\n");
}

function renderReport(report) {
  const lines = [
    "# Agent Estate Audit Report",
    "",
    "## Summary",
    "",
    bullet(`Schema version: ${report.schemaVersion}`),
    bullet(`Source: ${report.metadata.source}`),
    bullet(`Generated by: ${report.metadata.generator} ${report.metadata.generatorVersion}`),
    bullet(`Workspace type: ${report.environment.workspaceType}`),
    bullet(`Agents: ${report.agents.length}`),
    bullet(`MCP servers: ${report.mcpServers.length}`),
    bullet(`Risk findings: ${report.riskFindings.length}`),
    "",
    "## Environment",
    "",
    bullet(`Platform: ${report.environment.platform}`),
    bullet(`Workspace root: ${report.metadata.workspaceRoot ?? "not recorded"}`),
    "",
    "## Project Signals",
    ""
  ];

  for (const signal of report.environment.detectedProjectSignals) {
    lines.push(bullet(`${signal.label} [${signal.kind}]${signal.path ? ` - ${signal.path}` : ""}`));
  }

  lines.push("", "## Agents", "");
  for (const agent of report.agents) {
    lines.push(`### ${agent.name}`, "", bullet(`Kind: ${agent.kind}`), bullet(`Risk: ${riskBadge(agent.riskLevel)}`), bullet(`Surfaces: ${agent.riskSurfaces.join(", ") || "none"}`), renderEvidence(agent.evidence), "");
  }

  lines.push("## MCP Servers", "");
  for (const server of report.mcpServers) {
    lines.push(`### ${server.name}`, "", bullet(`Transport: ${server.transport}`), bullet(`Risk: ${riskBadge(server.riskLevel)}`), bullet(`Tools: ${server.declaredTools.join(", ") || "none declared"}`), renderEvidence(server.evidence), "");
  }

  lines.push("## Risk Findings", "");
  for (const finding of report.riskFindings) {
    lines.push(`### ${finding.title}`, "", bullet(`Level: ${riskBadge(finding.level)}`), bullet(`Surfaces: ${finding.surfaces.join(", ")}`), "", finding.summary, "", renderEvidence(finding.evidence), "");
  }

  lines.push("## eGovFrame Checklist", "");
  for (const item of report.egovFrameChecklist) {
    lines.push(bullet(`${item.label}: ${item.status}`));
  }

  lines.push("", "## Recommendations", "");
  for (const recommendation of report.recommendations) {
    lines.push(`### ${recommendation.title}`, "", bullet(`Priority: ${recommendation.priority}`), "", recommendation.rationale, "", `Action: ${recommendation.action}`, "");
  }

  return `${lines.join("\n").trim()}\n`;
}

const report = readJson(reportPath);
const markdown = renderReport(report);

if (!markdown.includes("# Agent Estate Audit Report")) {
  throw new Error("Rendered report is missing the title.");
}

if (!markdown.includes("## Risk Findings") || !markdown.includes("## eGovFrame Checklist")) {
  throw new Error("Rendered report is missing required audit sections.");
}

if (args.has("--check")) {
  console.log("Markdown report rendering check passed.");
} else {
  writeFileSync(outputPath, markdown);
  console.log(`Wrote ${path.relative(root, outputPath)}`);
}
